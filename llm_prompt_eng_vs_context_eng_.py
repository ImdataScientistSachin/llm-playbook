# --- Pydantic Validation + Retry Loop (production pattern) ---


# 1. Define schema as a Pydantic model
# 2. Generate output
# 3. Try to parse and validate
# 4. If validation fails, send the error back to the model for self-correction
# This self-healing loop handles ~95% of edge cases automatically.

from pydantic import BaseModel, Field, ValidationError
from typing import Literal

class RPGCharacter(BaseModel):
    name: str
    character_class: Literal['warrior', 'mage', 'rogue', 'paladin']
    level: int = Field(..., ge=1, le=20)
    hp: int = Field(..., gt=0)
    weapon: str
    armor: str
    backstory: str


def generate_structured(prompt: str, model_class, max_retries: int = 3):
    """Generate JSON with Pydantic validation and self-healing retry."""
    schema_str = json.dumps(model_class.model_json_schema(), indent=2)
    messages = [{'role': 'user', 'content': (
        f'{prompt}\n\nRespond ONLY with valid JSON matching this schema:\n{schema_str}\n'
        'No markdown. No extra text. Valid JSON only.'
    )}]

    for attempt in range(1, max_retries + 1):
        raw = generate(messages, max_new_tokens=300, do_sample=False)
        clean = re.sub(r'^```json\s*|```$', '', raw.strip(), flags=re.MULTILINE).strip()
        try:
            return model_class(**json.loads(clean))
        except (json.JSONDecodeError, ValidationError) as e:
            print(f'  Attempt {attempt} failed: {e}')
            if attempt < max_retries:
                # Self-correction: tell model exactly what went wrong
                messages += [
                    {'role': 'assistant', 'content': clean},
                    {'role': 'user', 'content': f'Invalid JSON. Error: {e}. Fix and return ONLY valid JSON.'}
                ]
    return None


char = generate_structured('Create an interesting RPG character.', RPGCharacter)
