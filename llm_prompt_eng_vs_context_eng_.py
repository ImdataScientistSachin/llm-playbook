"""---
# Section 6 — Context Engineering

> *"Context engineering is the delicate art and science of filling the context window with just the right information for the next step."* — Andrej Karpathy

## The Upgrade from Prompt Engineering

Prompt engineering optimizes *what you say*. Context engineering optimizes *everything the model knows when it speaks*.

```
CONTEXT WINDOW CONTENTS:
  [System Prompt] + [Memory] + [Retrieved Docs] + [History] + [Query]
      fixed          dynamic      dynamic           managed    current
```

## The 4 CE Strategies

| Strategy | What it does | Implementation |
|----------|-------------|----------------|
| **WRITE** | Save info outside the window | Memory DB, scratchpad |
| **SELECT** | Fetch only relevant info | RAG retrieval |
| **COMPRESS** | Reduce token footprint | Summarize old history |
| **ISOLATE** | Separate different contexts | Sub-agents, structured state |

"""

# --- Token budget tracking ---


# First step in CE: measure what you are actually spending.
# Most developers have no idea how many tokens their context consumes.

@dataclass
class ContextBudget:
    total_limit: int = 4096
    system_prompt: int = 500
    memory: int = 300
    retrieved_docs: int = 1500
    conversation: int = 1000
    query: int = 300
    response_reserve: int = 500

    def allocated(self) -> int:
        return self.system_prompt + self.memory + self.retrieved_docs + self.conversation + self.query + self.response_reserve

    def is_valid(self) -> bool:
        return self.allocated() <= self.total_limit


def count_tokens(text: str) -> int:
    return len(tokenizer.encode(text))


def audit_context(messages: list[dict], budget: ContextBudget) -> dict:
    """Audit token usage of an assembled context."""
    total = sum(count_tokens(m['content']) for m in messages)
    available = budget.total_limit - budget.response_reserve
    return {'total_tokens': total, 'available': available,
            'used_pct': f'{total/available*100:.1f}%', 'over_budget': total > available}


