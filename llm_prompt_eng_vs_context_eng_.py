# --- Simple RAG: SELECT strategy ---

# Instead of stuffing ALL documents in the context (impossible),
# retrieve ONLY the relevant chunks at query time.
# In production: replace SentenceTransformers with a vector DB (FAISS, Chroma, Pinecone).

from sentence_transformers import SentenceTransformer
import numpy as np


class SimpleRAG:
    """Minimal RAG using sentence-transformers + cosine similarity."""

    def __init__(self, embedding_model: str = 'all-MiniLM-L6-v2'):
        self.embedder = SentenceTransformer(embedding_model)
        self.documents: list[str] = []
        self.embeddings = None

    def index(self, documents: list[str]):
        self.documents = documents
        self.embeddings = self.embedder.encode(documents, normalize_embeddings=True)
        print(f'Indexed {len(documents)} documents')

    def retrieve(self, query: str, top_k: int = 3):
        q_emb = self.embedder.encode([query], normalize_embeddings=True)
        scores = (q_emb @ self.embeddings.T).squeeze()
        top_idx = np.argsort(scores)[::-1][:top_k]
        return [(float(scores[i]), self.documents[i]) for i in top_idx]

    def answer(self, query: str, score_threshold: float = 0.3) -> str:
        chunks = [(s, d) for s, d in self.retrieve(query) if s >= score_threshold]
        if not chunks:
            context_block = 'No relevant documents found.'
        else:
            # Most relevant chunk FIRST — anti-lost-in-middle
            context_block = '\n\n'.join(f'[Source {i+1} | Score:{s:.2f}]\n{d}' for i,(s,d) in enumerate(chunks))
        system = f'Answer based only on provided context. If not in context, say so.\n\nContext:\n{context_block}'
        return generate([{'role': 'system', 'content': system}, {'role': 'user', 'content': query}], max_new_tokens=200)


