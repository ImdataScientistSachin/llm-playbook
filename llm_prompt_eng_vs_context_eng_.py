# --- Technique selector: programmatic decision framework ---


def recommend_technique(requirements: dict) -> list[str]:
    """Map application requirements to the appropriate LLM techniques."""
    recs = ['Prompt Engineering']  # always the baseline
    if requirements.get('needs_private_or_live_data'):
        recs.append('RAG (Retrieval-Augmented Generation)')
    if requirements.get('output_must_be_parseable'):
        recs.append('Structured Generation (Pydantic + schema prompt)')
    if requirements.get('needs_session_memory'):
        recs.append('Memory System (vector DB + entity store)')
    if requirements.get('multi_step_reasoning'):
        recs.append('Chain-of-Thought or Tree-of-Thought')
    if requirements.get('autonomous_task_execution'):
        recs.append('Agentic Workflow (ReAct / Plan-and-Execute)')
    if requirements.get('context_window_near_limit'):
        recs.append('Context Engineering (compression + selective injection)')
    return recs


