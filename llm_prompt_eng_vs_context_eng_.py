# --- Conversation Manager: COMPRESS strategy ---

# Without management, history tokens grow linearly and hit the limit at ~turn 25.
# Pattern: keep last N turns in full, compress older turns into a summary.

class ConversationManager:
    """Sliding window history manager with automatic compression."""

    def __init__(self, max_full_turns: int = 6):
        self.max_full_turns = max_full_turns
        self.history: list[dict] = []
        self.summary: str = ''

    def add(self, role: str, content: str):
        self.history.append({'role': role, 'content': content})
        if len(self.history) > self.max_full_turns * 2:
            self._compress_oldest()

    def get_messages(self, system_prompt: str = '') -> list[dict]:
        msgs = []
        if system_prompt:
            msgs.append({'role': 'system', 'content': system_prompt})
        if self.summary:
            msgs.append({'role': 'system', 'content': f'[Earlier context]\n{self.summary}'})
        msgs.extend(self.history[-self.max_full_turns * 2:])
        return msgs

    def _compress_oldest(self):
        old = self.history[:-(self.max_full_turns * 2)]
        turn_text = '\n'.join(f"{t['role']}: {t['content']}" for t in old)
        new_summary = generate(
            [{'role': 'user', 'content': f'Summarize in 80 words, preserve all facts and decisions:\n\n{turn_text}'}],
            max_new_tokens=120, do_sample=False
        )
        self.summary = f'{self.summary}\n{new_summary}'.strip() if self.summary else new_summary.strip()
        self.history = self.history[-(self.max_full_turns * 2):]

    def token_count(self) -> int:
        return count_tokens(self.summary + ' '.join(t['content'] for t in self.history))


