"""---
# Section 2 — Decoding Strategies

## The most important inference-time knob

After the model computes a probability distribution over the next token, it must **choose** which token to emit. That choice algorithm is the decoding strategy.

| Strategy | Mechanism | Output | Best For |
|----------|-----------|--------|---------|
| **Greedy** | Always pick highest-prob token | Deterministic | Extraction, classification, QA |
| **Temperature** | Scale logits by 1/T before softmax | Higher T = more random | Creative tasks |
| **Top-k** | Sample from k highest-prob tokens | Bounded diversity | Variety without gibberish |
| **Top-p (nucleus)** | Sample from smallest set summing to p | Adaptive diversity | Most chat models |
| **Combined** | temp + top_p + top_k | Best of all | Production default |

**Math:** `p_i = softmax(z_i / T)`. T→0 = greedy. T→∞ = uniform random.


"""

