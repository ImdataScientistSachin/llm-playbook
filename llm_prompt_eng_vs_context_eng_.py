# --- Reusable Inference Wrapper ---

# we use One clean wrapper that:
#   - uses GenerationConfig (modern, validated, serializable)
#   - returns just the text string
#   - optionally tracks latency

def generate(
    messages: list[dict],
    *,
    max_new_tokens: int = 500,
    do_sample: bool = False,
    temperature: float = 1.0,
    top_p: float = 1.0,
    top_k: int = 50,
    repetition_penalty: float = 1.0,
    show_latency: bool = False,
) -> str:
    """Run inference and return the assistant reply as a string."""
    gen_cfg = GenerationConfig(
        max_new_tokens=max_new_tokens,
        do_sample=do_sample,
        temperature=temperature if do_sample else 1.0,
        top_p=top_p if do_sample else 1.0,
        top_k=top_k if do_sample else 50,
        repetition_penalty=repetition_penalty,
    )
    t0 = time.time()
    outputs = pipe(messages, generation_config=gen_cfg)
    elapsed = time.time() - t0
    reply = outputs[0]['generated_text']
    if show_latency:
        n_tok = len(tokenizer.encode(reply))
        print(f'  {elapsed:.2f}s | {n_tok} tokens | {n_tok/elapsed:.1f} tok/s')
    return reply


