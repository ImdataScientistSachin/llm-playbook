"""---
## Section 4 - Building the Vector Index

### What VectorstoreIndexCreator does in one call
```
Step 1: CHUNK
  CharacterTextSplitter(chunk_size=1000, chunk_overlap=0)
  Splits each page Document into smaller chunks (~1000 chars)
  Separator hierarchy: tries paragraph breaks, then newlines, then spaces

Step 2: EMBED
  HuggingFaceEmbeddings() -> all-MiniLM-L6-v2 by default
  Each chunk text -> 384-dimensional float vector
  Runs locally on CPU, no API key required

Step 3: INDEX
  Stores all vectors in an in-memory FAISS flat index
  Supports cosine similarity search via .as_retriever()
```

### HuggingFaceEmbeddings default model
Default: `sentence-transformers/all-MiniLM-L6-v2`
- Dimension: 384 (smaller but fast)
- Speed: ~14,000 sentences/sec on CPU
- Size: ~90MB download
- Quality: good for general retrieval, MTEB score ~49.5

### CharacterTextSplitter vs RecursiveCharacterTextSplitter
| Splitter | Separator strategy | Best for |
|---|---|---|
| `CharacterTextSplitter` | Single separator only | Simple text, fast |
| `RecursiveCharacterTextSplitter` | Tries multiple separators in order | Most use cases |
| `TokenTextSplitter` | Splits on token count | When token limits matter |
| `SemanticChunker` | Splits at meaning boundaries | Best quality, slowest |

### chunk_overlap=0 - is this a problem?
With zero overlap, sentences spanning a chunk boundary are split.
For the Attention paper (complete paragraphs), it is usually acceptable.
For denser technical text, setting overlap=100-200 is safer.

"""

import time
from langchain_classic.indexes import VectorstoreIndexCreator
from langchain_community.vectorstores import FAISS
from langchain_huggingface import HuggingFaceEmbeddings
from langchain_text_splitters import CharacterTextSplitter

# ==========================================
# 1. EMBEDDING MODEL SETUP
# ==========================================

# We are using all-MiniLM-L6-v2 by default. It's lightweight (~90MB),
# fast on CPU, and yields a 384-dimension vector space.
# Upgrade Path: If retrieval quality bottlenecks, swap to 'BAAI/bge-base-en-v1.5'
# (768 dimensions, requires more compute but performs better on MTEB benchmarks).
embedding_model = HuggingFaceEmbeddings(
    model_name='sentence-transformers/all-MiniLM-L6-v2',
    model_kwargs={'device': 'cpu'},
    encode_kwargs={'normalize_embeddings': True},  # Enforces cosine similarity down the line
)

# ==========================================
# 2. CHUNKING STRATEGY


# CharacterTextSplitter cuts raw data into bite-sized segments.
# Note on Overlap: Setting chunk_overlap=0 can sever semantic context at boundaries.
# Production recommendation: Set to 100-200 to keep context fluid between chunks.
text_splitter = CharacterTextSplitter(
    chunk_size=1000,
    chunk_overlap=0,
)

# ==========================================
# 3. PIPELINE EXECUTION (FAISS INDEX EXTRACTION)

print("Building FAISS index...")
print("Pipeline: Segmenting documents âž” Computing vector embeddings âž” Indexing in FAISS")
print("(Note: Initial run will download the model weights locally if not cached)")

t0 = time.time()

# We explicitly pass vectorstore_cls=FAISS. Without this override,
# LangChain defaults to an un-indexed InMemoryVectorStore which drops native
# index operations and causes downstream metadata retrieval failures.

index = VectorstoreIndexCreator(
    embedding=embedding_model,
    text_splitter=text_splitter,
    vectorstore_cls=FAISS,
).from_loaders([loader])  # Assumes 'loader' is pre-initialized upstream

t1 = time.time()

# ==========================================
# 4. METRICS & VERIFICATION

# Extract the underlying, native C++ FAISS index structure to verify data integrity
raw_index = index.vectorstore.index

print("\n--- Index Build Successful ---")
print(f"Build Duration    : {t1 - t0:.2f}s")
print(f"Total Vectors     : {raw_index.ntotal}")
print(f"Vector Dimension  : {raw_index.d}")

# Test retrieval BEFORE connecting the LLM
# This is a critical debugging step: if retrieval is broken, the LLM cannot fix it

test_query = 'What is multi-head attention?'
test_retriever = index.vectorstore.as_retriever(search_kwargs={'k': 3})
retrieved = test_retriever.invoke(test_query)

print(f'=== TEST RETRIEVAL: "{test_query}" ===')
print(f'Retrieved {len(retrieved)} chunks:\n')
for i, doc in enumerate(retrieved):
    page = doc.metadata.get('page', '?')
    preview = doc.page_content[:200].replace('\n', ' ')
    print(f'[Chunk {i+1}] Page {page}')
    print(f'  {preview}...')
    print()

"""---
## Section 5 - Setting Up the Groq LLM (Qwen3-32B)

### What is Groq?
Groq is a cloud AI inference provider running LLMs on custom **LPU (Language Processing Unit)**
hardware. Key advantage: **extremely fast inference** - often 300-800 tokens/sec
compared to 50-100 tokens/sec on typical GPU cloud servers.


### What is Qwen3-32B?
Qwen3-32B is a 32-billion parameter model from Alibaba:

| Property | Value |
|---|---|
| Parameters | 32 billion |
| Context window | 32,768 tokens |
| License | Apache 2.0 (commercial use allowed) |
| Strengths | Reasoning, math, code, multilingual |
| Thinking mode | Supports extended chain-of-thought internally |

### Key ChatGroq parameters
| Parameter | Value | Meaning |
|---|---|---|
| `temperature=0` | 0 | Greedy (deterministic) - best for factual Q&A |
| `max_tokens=None` | None | No hard output length limit |
| `reasoning_format='parsed'` | parsed | Model thinks internally, returns clean answer |
| `max_retries=2` | 2 | Retry automatically on transient API errors |

### What is reasoning_format='parsed'?
Qwen3 has a built-in thinking mode similar to o1/o3:
- `'parsed'` -> model reasons internally, you get only the clean final answer
- `'raw'`    -> you see the full thinking trace plus the answer
- `None`     -> thinking disabled (faster, less accurate on hard problems)

"""

import os
from getpass import getpass

# Set Groq API key securely
# Option A (Google Colab with Secrets):
#   from google.colab import userdata
#   os.environ['GROQ_API_KEY'] = userdata.get('GROQ_API_KEY')

# Option B (any environment - prompts securely, input is hidden):
