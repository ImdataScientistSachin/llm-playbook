from langchain_groq import ChatGroq

# ChatGroq sends prompts to Groq cloud servers (no local GPU needed)
# The model (Qwen3-32B) runs on Groq LPU hardware in the cloud

llm = ChatGroq(
    model='qwen/qwen3-32b',
    temperature=0,              # greedy = deterministic, best for factual Q&A
    max_tokens=None,            # use model default output length
    reasoning_format='parsed',  # Qwen3 thinks internally, returns clean answer
    timeout=None,
    max_retries=2,
)

# Quick connection test before building the full chain
test = llm.invoke('In one sentence, what is the Transformer architecture?')
print("=== LLM CONNECTION TEST ===")
print(f"Model    : qwen/qwen3-32b via Groq API")
