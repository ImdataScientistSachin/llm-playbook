"""---
## Section 6 - Building the RetrievalQA Chain

### What RetrievalQA does step by step
```
User query string
      |
      v
[Retriever] embeds query -> FAISS similarity search -> top-k chunks (Documents)
      |
      v
[chain_type: 'stuff'] concatenates all chunk texts into one context string
      |
      v
[Prompt template] fills {context} and {question} placeholders
      |
      v
[LLM: Qwen3-32B] generates answer grounded in the retrieved context
      |
      v
Answer string
```

### Chain type options
| Type | How it works | Best for |
|---|---|---|
| `stuff` | Concatenate all chunks into one prompt | Short-medium context (under 8k tokens) |
| `map_reduce` | Summarize each chunk separately then combine | Very many chunks, very long docs |
| `refine` | Process chunks sequentially, refining the answer | When order of chunks matters |
| `map_rerank` | Score each chunk independently, pick highest | When one chunk has the complete answer |

### Why 'stuff' is correct here
The paper is 15 pages. With chunk_size=1000 we get roughly 60-80 chunks.
Top-4 retrieved chunks = ~4000 chars = well within Qwen3's 32k token context.
'stuff' is the simplest and best choice for this scale.



"""

from langchain_classic.chains import RetrievalQA

# Configure the retriever from our FAISS index
# k=4 -> return the 4 most similar chunks for each query
retriever = index.vectorstore.as_retriever(
    search_type='similarity',
    search_kwargs={'k': 4}
)

# RetrievalQA assembles: retriever + prompt template + LLM + output parser
#
# chain_type='stuff'        -> concatenate all 4 chunks into one prompt
# input_key='question'      -> input dict key for the user question
# return_source_documents   -> ADDED: see which chunks were used (critical for debugging)
# verbose=True              -> print intermediate steps for transparency

chain = RetrievalQA.from_chain_type(
    llm=llm,
    chain_type='stuff',
    retriever=retriever,
    input_key='question',
    return_source_documents=True,  # ADDED: essential for verifying grounding
    verbose=True,
)

print("RetrievalQA chain ready")
print("Pipeline: FAISS retriever -> stuff chain -> Qwen3-32B")

