# ── Pattern 2: Multi-Query Retrieval ─────────────────────────────────────────

# Problem: a single query may miss important aspects of a complex question.
# Solution: rephrase the query N ways, retrieve for each, deduplicate results.
#
# Example: 'Tell me about IRobot production' might miss chunks about:
# - The filming locations (if query vocabulary doesn't match)
# - The visual effects team
# - The IMAX format
# Multi-query covers all these by generating 3-5 targeted sub-queries.
#
# Cost: N extra embedding calls (cheap) — NO extra LLM calls if using rule-based rephrasings
# With LLM-generated rephrasings: 1 extra LLM call to generate the variants

def multi_query_retrieve(
    query: str,
    vector_store: FAISS,
    k: int = 4,
    n_queries: int = 4,
    verbose: bool = True,
) -> list[Document]:
    """Multi-query RAG: rephrase query N times, retrieve for each, deduplicate."""
    # Step 1: Generate N rephrasings using the LLM
    rephrase_prompt = (
        f'Generate {n_queries} different ways to search for the same information as this query.\n'
        f'Each rephrasing should use different vocabulary and structure.\n'
        f'Output ONLY the {n_queries} queries, one per line, no numbering.\n\n'
        f'Original query: {query}'
    )
    rephrasings_raw = llm.invoke(rephrase_prompt).content.strip()
    queries = [q.strip() for q in rephrasings_raw.split('\n') if q.strip()][:n_queries]
    queries = [query] + queries  # Include original query

    if verbose:
        print(f'Original: "{query}"')
        print(f'Generated rephrasings:')
        for q in queries[1:]:
            print(f'  - {q}')

    # Step 2: Retrieve for each query
    seen_contents = set()
    all_results = []

    for q in queries:
        results = vector_store.similarity_search(q, k=k)
        for doc in results:
            # Deduplicate using first 100 chars as fingerprint
            key = doc.page_content[:100]
            if key not in seen_contents:
                seen_contents.add(key)
                all_results.append(doc)

    if verbose:
        print(f'\nUnique chunks retrieved: {len(all_results)} (vs {k} from single query)')

    return all_results[:k * 2]  # Return at most 2x k unique chunks


