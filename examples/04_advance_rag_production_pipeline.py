# ── Production Split: RecursiveCharacterTextSplitter ─────────────────────────


# This is the LangChain standard
# It tries each separator in order — if the first doesn't produce small-enough chunks,
# it falls back to the next. This preserves natural boundaries as much as possible.
#
# Separator hierarchy:
#   '\n\n' → paragraph breaks (preserve most context)
#   '\n'   → line breaks
#   '. '   → sentence boundaries (note the space — avoids abbreviation splits)
#   ' '    → word boundaries (last resort)
#   ''     → character level (absolute last resort)

def make_splitter(chunk_size: int, chunk_overlap: int) -> RecursiveCharacterTextSplitter:
    """Factory for RecursiveCharacterTextSplitter — the production standard."""
    return RecursiveCharacterTextSplitter(
        chunk_size=chunk_size,
        chunk_overlap=chunk_overlap,
        separators=['\n\n', '\n', '. ', ' ', ''],
        length_function=len,           # count chars (not tokens — but consistent)
        add_start_index=True,          # adds metadata.start_index per chunk
    )


