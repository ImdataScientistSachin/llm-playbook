# ── Semantic Similarity Demo ───────────────────────────────────────────────────

#  demonstrates WHY embedding models work for retrieval.
# Semantically similar sentences should have high cosine similarity (close to 1.0).
# Unrelated sentences should have low similarity (close to 0.0).
# This is sanity check that the embedding model understands The domain.

import numpy as np


def cosine_similarity(v1: list[float], v2: list[float]) -> float:
    """Cosine similarity between two vectors. Range: -1 to 1 (1=identical)."""
    a, b = np.array(v1), np.array(v2)
    return float(np.dot(a, b) / (np.linalg.norm(a) * np.linalg.norm(b)))


