# ── Pattern 3: Contextual Compression ────────────────────────────────────────


# Problem: Retrieved chunks often contain information irrelevant to the specific query.
# A chunk about 'filming locations and cast' contains both 'Alberta' AND 'will smith'.

# If the query is only about filming locations, half the chunk is noise.
# Solution: After retrieval, pass each chunk through the LLM to extract ONLY
# the part relevant to the query. This reduces context noise.
# Tradeoff: N extra LLM calls (one per chunk). Only worth it when chunks are large
# and context window is limited, or when faithfulness is critical.

def compress_chunk(query: str, chunk_content: str) -> str:
    """Extract only the query-relevant portion of a retrieved chunk."""
    compress_prompt = (
        f'Given this query: "{query}"\n\n'
        f'And this retrieved text:\n{chunk_content}\n\n'
        f'Extract ONLY the sentences directly relevant to the query.\n'
        f'If nothing is relevant, respond: NOT_RELEVANT\n'
        f'Relevant extract:'
    )
    compressed = llm.invoke(compress_prompt).content.strip()
    return compressed


def retrieve_and_compress(
    query: str,
    vector_store: FAISS,
    k: int = 3,
    verbose: bool = True,
) -> list[str]:
    """Retrieve chunks then compress each to query-relevant content."""
    raw_chunks = vector_store.similarity_search(query, k=k)
    compressed = []
    for doc in raw_chunks:
        c = compress_chunk(query, doc.page_content)
        if 'NOT_RELEVANT' not in c:
            compressed.append(c)
    if verbose:
        print(f'Compressed {len(raw_chunks)} chunks → {len(compressed)} relevant extracts')
    return compressed


