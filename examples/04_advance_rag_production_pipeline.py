# ── Evaluation Runner ─────────────────────────────────────────────────────────


# Runs the full golden test set and computes:
#   1. Retrieval Recall@k (did the right chunk get retrieved?)
#   2. Answer Accuracy (does the answer contain the expected keyword?)
# In production: use RAGAS library for deeper metrics (faithfulness, answer relevance)

def evaluate_rag(
    test_set: list[dict],
    rag_chain,
    vector_store: FAISS,
    k: int = 3,
) -> dict:
    """Evaluate the RAG pipeline on a golden test set."""
    retrieval_correct = 0
    answer_correct = 0
    total_latency = 0
    results = []

    for test in test_set:
        q = test['question']
        expected_kw = test['expected_keyword'].lower()

        # 1. Retrieval quality: does the right chunk appear in top-k?
        retrieved = vector_store.similarity_search(q, k=k)
        retrieved_text = ' '.join(r.page_content for r in retrieved).lower()
        retrieval_ok = expected_kw in retrieved_text
        retrieval_correct += int(retrieval_ok)

        # 2. Answer quality: does the final answer contain the expected keyword?
        t0 = time.time()
        answer = rag_chain.invoke(q)
        elapsed = time.time() - t0
        total_latency += elapsed

        answer_ok = expected_kw in answer.lower()
        answer_correct += int(answer_ok)

        results.append({
            'question': q,
            'answer': answer[:100],
            'retrieval_ok': retrieval_ok,
            'answer_ok': answer_ok,
            'latency': elapsed,
        })

        status = 'PASS' if (retrieval_ok and answer_ok) else 'FAIL'
        print(f'  [{status}] {q[:55]:55s} | ret={int(retrieval_ok)} ans={int(answer_ok)} | {elapsed:.2f}s')

    n = len(test_set)
    summary = {
        'retrieval_recall': retrieval_correct / n,
        'answer_accuracy': answer_correct / n,
        'avg_latency': total_latency / n,
        'results': results,
    }
    return summary


