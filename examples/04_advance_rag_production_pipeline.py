# Format retrieved documents into a clean context string.
# We include the source metadata so the model can acknowledge where info comes from.
# Proper formatting helps the LLM distinguish between different source chunks.

def format_docs(docs: list[Document]) -> str:
    """Format a list of Documents into a numbered context string with source attribution."""
    if not docs:
        return 'No relevant context found.'

