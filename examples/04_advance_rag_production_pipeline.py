"""---
## Section 9 — Advanced RAG Patterns

### Why basic RAG often isn't enough

Basic RAG (embed query → retrieve → generate) has a fundamental weakness:
**the embedding of the query may not match the embedding of the relevant document chunk**.

Reasons for mismatch:
- Query uses different vocabulary than the document
- Query is abstract; document is specific (or vice versa)
- Single query misses multiple relevant aspects of a complex question

### Three patterns that improve retrieval

| Pattern | Problem it solves | Mechanism |
|---------|------------------|----------|
| **HyDE** | Query/document embedding gap | Generate a hypothetical answer, embed that instead of the query |
| **Multi-query** | Single query misses aspects | Generate N rephrasings, retrieve for each, deduplicate |
| **Re-ranking** | ANN retrieval is approximate | Use a cross-encoder to re-score and re-rank retrieved chunks |


"""

# ── Pattern 1: HyDE (Hypothetical Document Embeddings) ───────────────────────


# Original paper: 'Precise Zero-Shot Dense Retrieval without Relevance Labels' (Gao et al. 2022)

# The insight: a question and its answer exist in very different embedding spaces.
# 'How much did IRobot earn?' maps differently than '$353.13 million worldwide gross'
# HyDE bridges this gap: generate a hypothetical answer, then embed THAT for retrieval.

# Tradeoff: adds one extra LLM call (latency + cost) but significantly improves recall
# for questions where the vocabulary gap between query and document is large.



def retrieve_with_hyde(query: str, vector_store: FAISS, k: int = 3) -> list[Document]:
    """HyDE: embed a generated hypothetical answer instead of the raw query."""

    # Step 1: Generate a hypothetical document that would answer this query
    hyde_prompt = (
        f'Write a factual passage that directly answers this question in 2-3 sentences:\n'
        f'Question: {query}\nPassage:'
    )
    hypothetical_doc = llm.invoke(hyde_prompt).content.strip()

    # Step 2: Embed the hypothetical document (NOT the original query)
    # The hypothetical doc uses the same vocabulary/style as real retrieved chunks
    results = vector_store.similarity_search(hypothetical_doc, k=k)

    return results, hypothetical_doc


