"""---
## Section 5 — LLM Setup: ChatGroq

### Why Groq for this notebook

Groq provides free, fast inference via API — ideal for RAG notebooks where
the focus is the retrieval pipeline, not the generation model.
The LPU (Language Processing Unit) hardware delivers much lower latency
than typical cloud GPU endpoints.

#### Temperature = 0 for RAG: why this matters

In RAG, the LLM's job is to **faithfully report what the context says**.
We do NOT want creative, surprising answers — we want accurate summaries of retrieved facts.

- `temperature=0`: Deterministic. Same context always gives the same answer. Maximally faithful.
- `temperature>0`: Introduces sampling variance. The model may paraphrase differently or introduce errors.

**Rule:** Use `temperature=0` for factual RAG. Use `temperature>0` for creative or summarization tasks.


"""

