# ── Retrieval with Score Filtering ────────────────────────────────────────────


# adds We score-based filtering function
#
# Why it matters: if no relevant chunk exists for a query, the retriever
# should return empty (or say 'I don't know') rather than hallucinating
# an answer from low-relevance chunks.
#
# FAISS returns L2 distances. We convert to cosine similarity here.
# For normalized vectors: cosine_sim = 1 - (l2_dist^2 / 2)

def retrieve_with_scores(
    query: str,
    vector_store: FAISS,
    k: int = 3,
    score_threshold: float = 0.3,
    verbose: bool = True,
) -> list[tuple[Document, float]]:
    """Retrieve top-k chunks with cosine similarity scores, filtered by threshold."""
    # similarity_search_with_score returns (Document, score) tuples
    # For FAISS with normalized embeddings, lower score = more similar (L2 distance)

    raw_results = vector_store.similarity_search_with_score(query, k=k * 2)

    # Convert L2 distance to cosine similarity and filter
    results = []
    for doc, l2_dist in raw_results:
        # cos_sim = 1 - (l2_dist^2 / 2) for normalized vectors
        cos_sim = 1.0 - (l2_dist ** 2) / 2.0
        if cos_sim >= score_threshold:
            results.append((doc, cos_sim))

    # Return top-k after filtering, sorted by similarity (highest first)
    results = sorted(results, key=lambda x: x[1], reverse=True)[:k]

    if verbose:
        print(f'Query: "{query[:60]}"')
        print(f'Retrieved {len(results)}/{k} chunks above threshold {score_threshold}')
        for i, (doc, score) in enumerate(results):
            print(f'  [{i}] score={score:.4f} | {doc.page_content[:80]}...')
        print()

    return results


