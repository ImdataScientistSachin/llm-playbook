"""---
## Section 4 — Vector Store: FAISS

### What a vector store does

A vector store is a database optimized for **approximate nearest neighbor (ANN) search**.
Given a query vector, it returns the K stored vectors most similar to it — fast, at scale.



**Recommendation:** FAISS for this notebook, Chroma for local production, Pinecone/Qdrant for cloud.

### Score threshold: the most overlooked FAISS setting

`similarity_search_with_score()` returns scores so we can filter

"""

# ── Build FAISS Vector Store ──────────────────────────────────────────────────

# FAISS.from_documents embeds all chunks and builds the ANN index in one call.
# Each chunk retains its metadata (source, title, start_index) in the store.
