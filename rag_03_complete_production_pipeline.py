# ── RAG Prompt Template ───────────────────────────────────────────────────────

# Prompt design principles for RAG:
# 1. Put the context BEFORE the question (anti-lost-in-middle: context near start)
# 2. Give an explicit 'no answer' instruction — prevents hallucination
# 3. Keep the instruction clear and direct
# 4. No model-specific tokens (<|user|> etc.) — use ChatPromptTemplate roles instead
# 5. Request citation where possible

RAG_PROMPT_TEMPLATE = """\
You are a precise question-answering assistant.
Use ONLY the following retrieved context to answer the question.
If the answer is not in the context, say exactly: "I cannot find this information in the provided context."
Do not add information from your training data.

Context:
{context}

Question: {question}

Answer:"""

rag_prompt = PromptTemplate(
    template=RAG_PROMPT_TEMPLATE,
    input_variables=['context', 'question'],
)

print('RAG prompt template created')
print('Key design choices:')
print('  - Context before question (anti-lost-in-middle)')
print('  - Explicit fallback instruction (prevents hallucination)')
print('  - No model-specific tokens (portable across LLMs)')

# ── Context Formatter ─────────────────────────────────────────────────────────

# Format retrieved documents into a clean context string.
# We include the source metadata so the model can acknowledge where info comes from.
# Proper formatting helps the LLM distinguish between different source chunks.

def format_docs(docs: list[Document]) -> str:
    """Format a list of Documents into a numbered context string with source attribution."""
    if not docs:
        return 'No relevant context found.'

    formatted = []
    for i, doc in enumerate(docs, 1):
        source = doc.metadata.get('source', 'unknown')
        formatted.append(
            f'[{i}] (Source: {source})\n{doc.page_content}'
        )
    return '\n\n'.join(formatted)


# Test the formatter
sample_docs = vector_store.similarity_search('film director and producer', k=2)
print('=== Formatted context example ===')
print(format_docs(sample_docs))



# ── LCEL RAG Chain (Modern Pattern) ──────────────────────────────────────────


# This is the clean, modern approach that replaces the deprecated RetrievalQA.
# LCEL chains are built with the | (pipe) operator — like Unix pipes for LLM steps.
#
# Chain steps:
#   1. RunnableParallel: runs retriever + passthrough simultaneously
#   2. rag_prompt: formats context + question into the template
#   3. llm: generates the answer
#   4. StrOutputParser: extracts the string content from the AIMessage object

retriever = vector_store.as_retriever(
    search_type='similarity',
    search_kwargs={'k': cfg.retrieval_k}
)

# Build the chain using LCEL pipe syntax
rag_chain = (
    RunnableParallel(
        context=retriever | format_docs,   # retrieve → format
        question=RunnablePassthrough(),     # pass the query straight through
    )
    | rag_prompt                           # inject into prompt template
    | llm                                  # generate
    | StrOutputParser()                    # extract string from AIMessage
)

print('RAG chain built using LCEL (modern LangChain)')
print('Chain steps: Retriever → Format → Prompt → LLM → Parse')

# Also build a chain that returns sources alongside the answer
rag_chain_with_sources = RunnableParallel(
    answer=rag_chain,
    sources=retriever,
)

print('Source-tracking chain also built')

# ── Query Runner ──────────────────────────────────────────────────────────────

# A clean wrapper that runs a query and displays formatted output.
# Reusable for all query experiments in this notebook.

def run_rag_query(
    question: str,
    chain=None,
    show_sources: bool = True,
    label: str = 'RAG',
) -> dict:
    """Run a RAG query and return answer + sources + latency."""
    if chain is None:
        chain = rag_chain_with_sources

    t0 = time.time()
    result = chain.invoke(question)
    elapsed = time.time() - t0

    answer = result.get('answer', result) if isinstance(result, dict) else result
    sources = result.get('sources', []) if isinstance(result, dict) else []

    print(f'[{label}] Q: {question}')
    print(f'  Answer ({elapsed:.2f}s):')
    print(textwrap.fill(answer.strip(), width=80, initial_indent='    ', subsequent_indent='    '))

    if show_sources and sources:
        print(f'  Sources used ({len(sources)} chunks):')
        for src in sources:
            print(f'    • {src.page_content[:70]}...')
    print()
    return {'question': question, 'answer': answer, 'sources': sources, 'latency': elapsed}


